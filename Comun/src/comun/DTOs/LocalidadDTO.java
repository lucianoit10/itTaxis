package comun.DTOs;

public class LocalidadDTO {
	private long id;
	private String nombre;
	
	public LocalidadDTO() {
		super();
	}
	
	public long getId() {
		return id;
	}
	
	public void setId(long id) {
		this.id = id;
	}
	
	public String getNombre() {
		return nombre;
	}
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}
